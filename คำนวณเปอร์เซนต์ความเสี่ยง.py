def calculate_cost(x1, x2, x3):
    # ค่าสัมประสิทธิ์จากตารางในหน้า 453
    intercept = 63.752
    b1 = 0.954
    b2 = 2.086
    b3 = 0.520
    
    # สมการ: y = a + b1*x1 + b2*x2 + b3*x3
    y = intercept + (b1 * x1) + (b2 * x2) + (b3 * x3)
    return y

# --- ส่วนของการรับค่าและแสดงผล ---
print("--- โปรแกรมพยากรณ์ต้นทุน (Y) ---")

# รับค่าจากผู้ใช้ผ่านคีย์บอร์ด (input จะถูกเปลี่ยนเป็นตัวเลขด้วย float)
val_x1 = float(input("กรอกปริมาณกระดาษ (x1): "))
val_x2 = float(input("กรอกเวลาเครื่องจักร (x2): "))
val_x3 = float(input("กรอกต้นทุนคงที่ (x3): "))

# เรียกใช้ฟังก์ชันและเก็บผลลัพธ์ในตัวแปร result
result = calculate_cost(val_x1, val_x2, val_x3)

# แสดงผลลัพธ์ที่คำนวณได้
print(f"\nต้นทุนพยากรณ์ (Y) คือ: {result:.3f}")
print(f"คิดเป็นเงินประมาณ: {result * 1000:,.2f} บาท")